// logical and
function testLogicalAnd (val) {
    if (val<=50 && val>=25) { 
        return "yes";
    }
    return "no";
}
console.log(testLogicalAnd(29));






// logical or 
function logicalOr (val){
    if (val >= 100 || val <=50){ 
        return "yes";
    }
    return "no";
}
